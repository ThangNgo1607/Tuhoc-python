# Hàm lấy số ngẫu nhiên random: randrange(x,y)
from random import randrange
a = randrange(200)
print(a)



# có 2 cách để lấy ra random:
#cách 1: sử dụng: from random import randrange => cú pháp trong lệnh sẽ là " biến = randrand(x,y)"
#cách 2: dủ dụng: import random => cú pháp sẽ là " biến = random.randrange(x,y)"